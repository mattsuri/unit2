grade = int(input("Enter your grade: "))

if grade > 90:
    print("You earned an A")
elif grade > 80:
    print("You earned an B")
elif grade > 70:
    print("You earned an C")
elif grade > 60:
    print("You earned an D")
else:
    print("You earned an F")
